package easy;

public class ConvertSortedArraytoBST_108 {
	public static class TreeNode {
		int val;
		TreeNode left;
		TreeNode right;

		TreeNode() {
		}

		TreeNode(int val) {
			this.val = val;
		}

		TreeNode(int val, TreeNode left, TreeNode right) {
			this.val = val;
			this.left = left;
			this.right = right;
		}
	}

	public static TreeNode sortedArrayToBST(int[] nums) {
		TreeNode root = new TreeNode();
		return root;
	}

}
